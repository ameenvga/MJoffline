function getEmojees(emoTitle, emoList){
        console.log('distributing emojees')
        
        changeBGColor(emoTitle)//changing emoji tabs color based selected
        document.getElementById('emojiBoard').innerHTML = ''; //resetting emojiboard
        for (var i=0; i <  emoList.length; i++){
            var el = document.createElement('div')
            el.classList.add('emojiIcon')    
            el.innerHTML = emoList[i]
            document.getElementById('emojiBoard').appendChild(el)
        }
        var emojeees = document.getElementsByClassName('emojiIcon')
    
        for (var n=0; n < emojeees.length; n++){
            emojeees[n].addEventListener('click', function(event){
                insertAtCursor(textarea, event.target.innerHTML)

            })
        }
    }
